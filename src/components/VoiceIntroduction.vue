<script setup>
import { ref, onMounted, onUnmounted, watch, computed } from 'vue'
import { useRouter } from 'vue-router'
import { useLanguage } from '../composables/useLanguage.js'
import { useTheme } from '../composables/useTheme.js'

// Router 實例
const router = useRouter()

// 語言支援
const { t, currentLanguage } = useLanguage()

// 主題支援
const { isDark } = useTheme()

// 自我介紹內容（根據語言動態切換）
const introText = computed(() => t('introText'))

// 語音導覽時間軸配置（根據語言動態切換）
const navigationTimeline = computed(() => [
  {
    time: 0,
    actions: [
      { action: 'route', target: '/experience', description: t('jumpToExperience') },
      { action: 'delayed_scroll', target: '/experience#work-experience', delay: 500, description: t('currentPosition') }
    ]
  },
  { time: 13, action: 'scroll', target: '/experience#education', description: t('continuingEducation') },
  { time: 20, action: 'scroll', target: '/experience#tutorABC', description: t('shanghaExperience') },
  {
    time: 30,
    actions: [
      { action: 'route', target: '/contact', description: t('jumpToContact') },
      { action: 'delayed_scroll', target: '/contact#top', delay: 300, description: t('scrollToTop') }
    ]
  }
])

// 將文字分段，便於顯示當前播放內容
const sentences = computed(() => {
  const text = introText.value
  // 根據語言使用不同的分隔符
  const separator = currentLanguage.value === 'zh' ? /[。！？]/ : /[.!?]/
  return text.split(separator).filter(s => s.trim())
})

// 響應式數據
const isExpanded = ref(false)
const isPlaying = ref(false)
const speechSupported = ref(false)
const volume = ref(0.8)
const currentTime = ref(0)
const totalDuration = ref(35) // 預估總時長（秒）
const progressPercentage = ref(0)
const currentSentence = ref('')
const currentSentenceIndex = ref(0)

// 語音合成相關
let speechSynthesis = null
let utterance = null
let progressTimer = null

// 檢查瀏覽器支援
onMounted(() => {
  speechSupported.value = 'speechSynthesis' in window
  if (speechSupported.value) {
    speechSynthesis = window.speechSynthesis
    
    // 確保語音列表加載完成
    const loadVoices = () => {
      const voices = speechSynthesis.getVoices()
      if (voices.length > 0) {
        console.log('=== 可用語音列表 ===')
        voices.forEach((voice, index) => {
          console.log(`${index + 1}. ${voice.name} (${voice.lang}) - ${voice.gender || 'unknown gender'}`)
        })
        console.log('==================')
        
        // 顯示中文和英文語音
        const zhVoices = voices.filter(v => v.lang.includes('zh'))
        const enVoices = voices.filter(v => v.lang.includes('en'))
        
        console.log('中文語音:', zhVoices.map(v => v.name))
        console.log('英文語音:', enVoices.map(v => v.name))
      }
    }
    
    if (speechSynthesis.getVoices().length === 0) {
      speechSynthesis.addEventListener('voiceschanged', loadVoices)
    } else {
      loadVoices()
    }
  }
})

// 監聽音量變化
watch(volume, (newVolume) => {
  if (utterance) {
    utterance.volume = newVolume
  }
})

// 切換介紹面板
const toggleIntro = () => {
  isExpanded.value = !isExpanded.value
  if (isExpanded.value) {
    startPlayback()
  }
}

// 關閉面板
const closePanel = () => {
  stopPlayback()
  isExpanded.value = false
}

// 開始播放
const startPlayback = () => {
  if (!speechSupported.value) return

  // 創建語音合成實例
  utterance = new SpeechSynthesisUtterance(introText.value)

  // 設置語音參數
  utterance.rate = 0.9 // 稍微慢一點
  utterance.volume = volume.value
  
  // 根據主題和語言設定語音參數
  if (isDark.value) {
    // 深色模式：使用更低沉的音調，模擬黑人語音特色
    utterance.pitch = 0.8
    utterance.rate = 0.85
    utterance.lang = currentLanguage.value === 'zh' ? 'zh-TW' : 'en-US'
    
    // 嘗試選擇特定的語音（如果可用）
    const selectVoice = () => {
      if (!speechSynthesis || !speechSynthesis.getVoices) return
      
      const voices = speechSynthesis.getVoices()
      if (voices.length === 0) {
        // 如果語音列表還沒載入，等待一下再試
        setTimeout(selectVoice, 100)
        return
      }
      
      let selectedVoice = null
      
      if (currentLanguage.value === 'zh') {
        // 中文：尋找男性或低音調語音
        const preferredNames = ['Xiaoyun', '小雲', 'Male', '男', 'Kangkang', '康康', 'Yaoyao', '瑤瑤']
        selectedVoice = voices.find(voice => 
          voice.lang.includes('zh') && 
          preferredNames.some(name => voice.name.includes(name))
        )
        
        // 如果沒找到，就選第一個中文男性語音
        if (!selectedVoice) {
          selectedVoice = voices.find(voice => 
            voice.lang.includes('zh') && voice.name.toLowerCase().includes('male')
          )
        }
      } else {
        // 英文：尋找低音調或黑人特色語音
        const preferredNames = [
          'Aaron', 'David', 'Daniel', 'Michael', 'James', 'Ravi', 'Alex',
          'Microsoft David', 'Google US English', 'Chrome OS US English',
          'Microsoft Mark', 'Microsoft Zira'
        ]
        
        selectedVoice = voices.find(voice => 
          voice.lang.includes('en') && 
          preferredNames.some(name => voice.name.includes(name))
        )
        
        // 如果沒找到，嘗試找任何男性英文語音
        if (!selectedVoice) {
          selectedVoice = voices.find(voice => 
            voice.lang.includes('en') && 
            (voice.name.toLowerCase().includes('male') || 
             voice.name.toLowerCase().includes('man') ||
             voice.name.toLowerCase().includes('david') ||
             voice.name.toLowerCase().includes('alex'))
          )
        }
      }
      
      if (selectedVoice) {
        utterance.voice = selectedVoice
        console.log(`深色模式選擇語音: ${selectedVoice.name} (${selectedVoice.lang})`)
      } else {
        console.log('未找到合適的深色模式語音，使用預設語音')
      }
    }
    
    // 執行語音選擇
    selectVoice()
  } else {
    // 淺色模式：使用正常音調
    utterance.pitch = 1.0
    utterance.lang = currentLanguage.value === 'zh' ? 'zh-TW' : 'en-US'
    console.log('淺色模式使用預設語音設定')
  }

  // 重置導覽時間軸
  resetNavigationTimeline()

  // 設置事件監聽
  utterance.onstart = () => {
    isPlaying.value = true
    currentTime.value = 0
    currentSentenceIndex.value = 0
    startProgressTimer()
  }

  utterance.onend = () => {
    isPlaying.value = false
    currentTime.value = 0
    progressPercentage.value = 0
    currentSentence.value = ''
    clearProgressTimer()
  }

  utterance.onerror = () => {
    isPlaying.value = false
    clearProgressTimer()
  }

  // 開始播放
  speechSynthesis.speak(utterance)
}

// 切換播放/暫停
const togglePlayback = () => {
  if (!speechSupported.value) return

  if (isPlaying.value) {
    speechSynthesis.pause()
    isPlaying.value = false
    clearProgressTimer()
  } else if (speechSynthesis.paused) {
    speechSynthesis.resume()
    isPlaying.value = true
    startProgressTimer()
  } else {
    startPlayback()
  }
}

// 停止播放
const stopPlayback = () => {
  if (speechSynthesis) {
    speechSynthesis.cancel()
  }
  isPlaying.value = false
  currentTime.value = 0
  progressPercentage.value = 0
  currentSentence.value = ''
  currentSentenceIndex.value = 0
  clearProgressTimer()
  resetNavigationTimeline()
}

// 進度計時器
const startProgressTimer = () => {
  clearProgressTimer()
  progressTimer = setInterval(() => {
    if (isPlaying.value) {
      currentTime.value += 0.1
      progressPercentage.value = (currentTime.value / totalDuration.value) * 100

      // 更新當前播放的句子
      const sentenceIndex = Math.floor((currentTime.value / totalDuration.value) * sentences.value.length)
      if (sentenceIndex < sentences.value.length && sentenceIndex !== currentSentenceIndex.value) {
        currentSentenceIndex.value = sentenceIndex
        currentSentence.value = sentences.value[sentenceIndex].trim()
      }

      // 檢查是否需要執行導覽動作
      checkNavigationTimeline()
    }
  }, 100)
}

// 檢查導覽時間軸
const checkNavigationTimeline = () => {
  const currentTimeSeconds = Math.floor(currentTime.value)

  navigationTimeline.value.forEach(item => {
    // 在指定時間點執行動作（允許 1 秒誤差）
    if (Math.abs(currentTimeSeconds - item.time) <= 1 && !item.executed) {
      item.executed = true

      if (item.actions) {
        // 多個動作
        item.actions.forEach(action => executeNavigationAction(action))
      } else {
        // 單一動作
        executeNavigationAction(item)
      }
    }
  })
}

// 執行導覽動作
const executeNavigationAction = (item) => {
  if (item.action === 'route') {
    // 切換到指定路由
    const targetPath = item.target.split('#')[0]
    if (router.currentRoute.value.path !== targetPath) {
      router.push(item.target)
    }
  } else if (item.action === 'scroll') {
    // 頁面內錨點滑動
    const [path, anchor] = item.target.split('#')

    // 先確保在正確的頁面
    if (router.currentRoute.value.path !== path) {
      router.push(path).then(() => {
        // 頁面切換後再滑動到錨點
        setTimeout(() => scrollToAnchor(anchor), 300)
      })
    } else {
      // 已在目標頁面，直接滑動
      scrollToAnchor(anchor)
    }
  } else if (item.action === 'delayed_scroll') {
    // 延遲滑動（用於在頁面切換後滑動）
    const delay = item.delay || 300
    setTimeout(() => {
      const anchor = item.target.split('#')[1]
      if (anchor === 'top') {
        // 滑動到頁面頂部
        window.scrollTo({ top: 0, behavior: 'smooth' })
      } else {
        scrollToAnchor(anchor)
      }
    }, delay)
  }
}

// 滑動到指定錨點
const scrollToAnchor = (anchorId) => {
  if (anchorId === 'top') {
    // 滑動到頁面頂部
    window.scrollTo({ top: 0, behavior: 'smooth' })
    return
  }

  const element = document.getElementById(anchorId)
  if (element) {
    element.scrollIntoView({
      behavior: 'smooth',
      block: 'start',
      inline: 'nearest'
    })
  }
}

// 重置導覽時間軸
const resetNavigationTimeline = () => {
  navigationTimeline.value.forEach(item => {
    item.executed = false
  })
}

const clearProgressTimer = () => {
  if (progressTimer) {
    clearInterval(progressTimer)
    progressTimer = null
  }
}

// 格式化時間
const formatTime = (seconds) => {
  const mins = Math.floor(seconds / 60)
  const secs = Math.floor(seconds % 60)
  return `${mins}:${secs.toString().padStart(2, '0')}`
}

// 清理
// 監聽語言切換
watch(currentLanguage, () => {
  // 如果正在播放，停止播放
  if (isPlaying.value) {
    stopPlayback()
  }
})

onUnmounted(() => {
  stopPlayback()
})
</script>

